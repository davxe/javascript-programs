//  =->it is assignment operator
//  ==->it is equality operator(it compares only values, not datatypes)
//  ===->it is strictly equality operator it compares the datatype first if it is true then compare the vallues


console.log('10'==10)//true
console.log('10'==20)//false
console.log('10'===10)//false
console.log('10'==='10')//true
console.log('10'==='20')//false